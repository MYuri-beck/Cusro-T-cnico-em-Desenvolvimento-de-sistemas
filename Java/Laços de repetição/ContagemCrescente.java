/*
Contagem Crescente:
Escreva um programa que utilize um laço for para imprimir os números de 1 a 10 no console.
*/
public class ContagemCrescente {

        
    public static void main(String args[]) {
       
        for (int i = 1; i <= 10; i++){
            System.out.println("Contagem: " + i);
        }
    }
}
